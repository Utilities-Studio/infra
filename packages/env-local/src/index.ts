#!/usr/bin/env bun
import { createHmac } from 'node:crypto'

const proc = Bun.spawn(['bunx', 'supabase', 'status', '-o', 'env'], {
	stdout: 'pipe',
	stderr: 'pipe',
})

const stdout = await new Response(proc.stdout).text()
const exitCode = await proc.exited

if (exitCode !== 0) {
	console.error('Local Supabase is not running. Start it with: bun dev:server')
	process.exit(1)
}

const env = Object.fromEntries(
	stdout
		.trim()
		.split('\n')
		.map((line) => {
			const [key, ...rest] = line.split('=')
			return [key, rest.join('=').replace(/^"|"$/g, '')]
		}),
)

const hookSecret = createHmac('sha256', env.JWT_SECRET)
	.update('send-email-hook')
	.digest('base64')

const content = `# ──────────────────────────────────────────────────────────────────────────────
# Local overrides -- auto-generated by env-local
# Points to local Supabase (bunx supabase start)
# ──────────────────────────────────────────────────────────────────────────────

# Vite (exposed to browser via import.meta.env)
VITE_SUPABASE_URL=${env.API_URL}
VITE_SUPABASE_PUBLISHABLE_KEY=${env.PUBLISHABLE_KEY}
VITE_SITE_URL=http://localhost:5173

# Supabase (server-only)
SUPABASE_URL=${env.API_URL}
SUPABASE_ANON_KEY=${env.PUBLISHABLE_KEY}
SUPABASE_SECRET_KEY=${env.SECRET_KEY}
SUPABASE_SERVICE_ROLE_KEY=${env.SECRET_KEY}
SUPABASE_DB_URL=${env.DB_URL}

# Email -- Supabase auth hook
SEND_EMAIL_HOOK_URI=${env.API_URL}/functions/v1/send-email
SEND_EMAIL_HOOK_SECRET=v1,whsec_${hookSecret}
`

await Bun.write('.env.development.local', content)
console.log('Generated .env.development.local from local Supabase')
